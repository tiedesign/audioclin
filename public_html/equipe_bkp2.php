                <h1>Equipe</h1>

				<br />

				<p>
					A clínica Audio Clin trabalha com profissionais otorrinolaringologistas e fonoaudiólogas.
				</p>
				<p>
					O otorrinolaringologista, também conhecido como Otorrino, é o profissional médico especializado no estudo, 
					prevenção e manutenção das funções dos ouvidos, nariz, seios da face e garganta. 
					Atende pacientes de todas as idades com queixas relacionadas à diminuição da audição e dificuldades de entendimento, 
					desconforto com sons, zumbido, tonturas, desequilíbrios, dor orofacial, alterações do revestimento da boca e da língua, 
					dificuldades na deglutição, alteração do olfato e paladar, halitose, dor de cabeça, tosse, obstrução nasal, ronco, 
					respiração oral, alergias, rouquidão, dor de garganta entre outras.
				</p>
				<p>
					O tratamento pode ser realizado através da orientação de mudança de hábitos, da indicação de medicamentos ou terapias fonoaudiológicas ou procedimentos cirúrgicos.
				</p>

				<p>
					O fonoaudiólogo, também chamado por "fono", é o profissional da área de saúde que realiza a avaliação e a reabilitação dos distúrbios da fala, 
					audição, voz, linguagem, escrita, leitura e demais problemas que afetam a comunicação humana. 
					Atuando em consultórios, clínicas, hospitais, postos de saúde, escolas e instituições especializadas, 
					o fonoaudiólogo desempenha um importante papel na integração social de pessoas com tais dificuldades.
				</p>
				<p>
					Pode também auxiliar profissionais que precisam da voz para executar determinadas atividades como professores, políticos, 
					locutores e artistas, além de elaborar programas de redução de ruído em fábricas e indústrias e
					reeducar músculos da cabeça e pescoço de portadores de aparelhos dentários, respiradores orais, entre outros.
					Trabalha na reabilitação auditiva com testes e adaptação de próteses auditivas.
				</p>

