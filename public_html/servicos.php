					<li>Vectoeletronistagmografia
						<div id="vectoeletronistagmografia" class="detailExam" title="Vectoeletronistagmografia">
					              <h2>O que é:</h2>
							<p>É um exame que avalia o labirinto (orelha interna), um dos órgãos responsáveis pelo equilíbrio. Os pacientes que realizam este exame apresentam queixas tais como: tontura, desequilíbrio, náuseas, vômito, "labirintite", entre outras.</p>
					              <h2>Como &eacute; feito:</h2>
							<p>O estímulo é visual (luz vermelha em uma barra a 1 metro de distância dos olhos do examinado). 
								A captação é elétrica, se dá através de eletrodos localizados do lado dos olhos e na testa do examinado. 
								Por último, realiza-se a prova calórica: o paciente deitado de olhos fechados, coloca-se primeiro água quente e depois água fria nos ouvidos por alguns segundos, 
								com o objetivo de estimular a tontura, registrá-la, e assim avaliar se encontra-se em níveis normais ou alterada.
							</p>
